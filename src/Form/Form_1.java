    private void btnCreateActionPerformed(ActionEvent evt) {
    // Database connection variables
    Connection conn = null;
    PreparedStatement pstmt = null;
    String url = "jdbc:mysql://localhost:3306/repairdb?useSSL=false";
    String driver = "com.mysql.cj.jdbc.Driver";
    String username = "root";
    String password = "1111";

    // Get the table name
    String tName = txtName.getText();
    
    // Determine the number of columns selected
    int numColumns = Integer.parseInt(cboColumn.getSelectedItem().toString());
    
    // Array to hold data for the columns
    String[][] columnData = new String[numColumns][4];
    
    // Gather data for each column using a loop
    for (int i = 0; i < numColumns; i++) {
        // Assuming you have text fields, combo boxes, and checkboxes named appropriately
        JTextField txtC = (JTextField) getComponentByName("txtC" + (i + 1)); // e.g., txtC1, txtC2, ...
        JComboBox cboType = (JComboBox) getComponentByName("cboType" + (i + 1)); // e.g., cboType1, cboType2, ...
        JCheckBox chkN = (JCheckBox) getComponentByName("chkN" + (i + 1)); // e.g., chkN1, chkN2, ...
        JCheckBox chkP = (JCheckBox) getComponentByName("chkP" + (i + 1)); // e.g., chkP1, chkP2, ...

        columnData[i] = gatherData(txtC.getText(), cboType.getSelectedItem().toString(), chkN.isSelected(), chkP.isSelected());
    }

    try {
        conn = DriverManager.getConnection(url, username, password);

        // Construct SQL CREATE TABLE statement
        StringBuilder sqlBuilder = new StringBuilder("CREATE TABLE " + tName + " (");
        
        for (int i = 0; i < numColumns; i++) {
            sqlBuilder.append(columnData[i][0]).append(" ").append(columnData[i][1]).append(" ")
                      .append(columnData[i][2]).append(" ").append(columnData[i][3]);
            if (i < numColumns - 1) {
                sqlBuilder.append(", "); // Add comma if not the last column
            }
        }
        
        sqlBuilder.append(")");

        pstmt = conn.prepareStatement(sqlBuilder.toString());
        pstmt.execute();
        
        JOptionPane.showMessageDialog(this, "Table Created with " + numColumns + " columns!!!");
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e);
    } finally {
        // Close resources in finally block to avoid resource leaks
        try {
            if (pstmt != null) pstmt.close();
            if (conn != null) conn.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }
}

// Method to gather data for each column
private String[] gatherData(String textFieldValue, String comboBoxValue, boolean isNull, boolean isPrimaryKey) {
    String st = isNull ? "NULL" : "NOT NULL";
    String pKey = isPrimaryKey ? "Primary Key" : "";
    return new String[]{textFieldValue, comboBoxValue, st, pKey};
}

// Utility method to get components by name (assuming you have a naming convention)
private Component getComponentByName(String name) {
    return Arrays.stream(this.getComponents())
                 .filter(c -> c.getName() != null && c.getName().equals(name))
                 .findFirst()
                 .orElse(null);
}


